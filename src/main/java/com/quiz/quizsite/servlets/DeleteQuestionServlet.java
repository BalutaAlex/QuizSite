package com.quiz.quizsite.servlets;

import com.quiz.quizsite.ejb.ManageQuestionsBean;
import com.site.quizsite.common.QuestionDetails;
import java.io.IOException;
import java.util.List;
import javax.annotation.security.DeclareRoles;
import javax.inject.Inject;
import javax.servlet.ServletException;
import javax.servlet.annotation.HttpConstraint;
import javax.servlet.annotation.ServletSecurity;
import javax.servlet.annotation.WebServlet;
import javax.servlet.http.HttpServlet;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;

/**
 *
 * @author Alex
 */

@DeclareRoles({"AdminRole"})
@ServletSecurity
        (
            value=@HttpConstraint
            (
            rolesAllowed={"AdminRole"}
            )
          
        )
@WebServlet(name = "DeleteQuestionServlet", urlPatterns = {"/DeleteQuestionServlet"})
public class DeleteQuestionServlet extends HttpServlet {

    @Inject
    ManageQuestionsBean manageQuestionsBean;

    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        
        
        List<QuestionDetails> questions = manageQuestionsBean.getAllQuestions();
        
        request.setAttribute("questions", questions);
        
        request.getRequestDispatcher("/WEB-INF/pages/deleteQuestion.jsp").forward(request, response);
        
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
           
        manageQuestionsBean.deteleQuestion(Integer.parseInt(request.getParameter("deleteQuestionBtn")));
        
        response.sendRedirect(request.getContextPath()+"/DeleteQuestionServlet");
        
    }

    /**
     * Returns a short description of the servlet.
     *
     * @return a String containing servlet description
     */
    @Override
    public String getServletInfo() {
        return "Short description";
    }// </editor-fold>

}
